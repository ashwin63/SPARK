<h2><b>About SPARK:</h2></b><br>
<br>
Spark hospitals was launched on 30th November 1980 by late Dr.S.Sankaranarayan
<br>
Besides being the chairman of Spark hospitals, he was one of the leading gastroenrologist and a well versed laser and laparoscopic surgeon. His focus was to offer medical care to people from all walks of life, with a commitment to quality and affordability.
<br>
<br>
Spark hospitals which is a National Accreditation board for hospitals (NABH) certified, aims at providing standard health care with its world class facilities and infrastructure. Our state of art equipped technology and well qualified doctors and nurse practitioners contribute to the success of the institution by providing maximum care and support to the patients. Today we provide services in various branches of medicine and surgery with our 600 bedded super specialty hospital situated at shenoy nagar and R.A.Puram in Chennai, india. This fruition is not inadvertent but is the sheer outcome of perceptive participation and ethical practices undertaken by our team to achieve objects. 

